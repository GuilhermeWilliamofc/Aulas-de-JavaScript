let num = [5, 8, 2, 9, 3] // vetores no javascript aceita varios tipos diferentes no mesmo vetor ex: ['Guilherme', 18, 'masculino'], diferente de outras linguagens

console.log(`Nosso vetor é o ${num}`) // vetor também é chamado de array

num.push(7) // semelhante ao .append do python, para substituir algum indice: num[3] = 6

console.log(`Nosso vetor agora é ${num}`)

console.log(`O primeiro elemento do vetor é ${num[0]}`)

console.log(`o tamanho do vetor é ${num.length}`) // length é semelhante ao len do python

console.log(`O meu vetor em ordem crescente é ${num.sort()}`) // sort é semelhante ao sort do python
